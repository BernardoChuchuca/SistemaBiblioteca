    private void calculo(String tipos) {
        double precio = 0;
        double cal = 0;

        if (tipos.equals("D")) {

            precio = Double.parseDouble(prec.getText());

            cal = precio + 5;

        } else if (tipos.equals("I")) {
           precio = Double.parseDouble(prec.getText());

            cal = (precio*0.2) + precio+20; 

        }
        
        total.setText("$ "+ String.valueOf(cal));
    }

