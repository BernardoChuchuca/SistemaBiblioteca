    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        ArrayList<Libro> lix = new LibControler().listar();
        for (Libro j : lix) {
            if (j.getTitulo().equals(bust.getText()) || j.getAutor().equals(bust.getText())) {

                titu.setText(j.getTitulo());
                auto.setText(j.getAutor());
                edito.setText(j.getEdicion());
                prec.setText(String.valueOf(j.getPrecio()));

                break;
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

